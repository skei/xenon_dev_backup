/* creates a circular spot in the center of a 256x256 area */
void MakeSpot(void)
{
 int x,y;
 long dist;
 unsigned char *p=PhongTexture;
 for (y=0;y<256;y++)
 {
  for (x=0;x<256;x++)
  {
   long c;
   int dist;
   /* get the distance from the center (128,128) of the spot */
   dist=sqrt((long)(y-128)*(long)(y-128)+(long)(x-128)*(long)(x-128));
   if (dist>128) dist=128;
   /* use the intensity table for the color at a given distance */
   *p++=PhongIntensity[128-dist];
  }
 }
}

