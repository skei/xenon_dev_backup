void CalcIsoValues( float strength)
 {
  int i,j,k,n;
  float x,y,z;
  float delta;
  float effect,dist;
  float inv_cube_strength, inv_sq_strength;

  delta = CubeSize/SubDiv;
  inv_cube_strength = 1/CUBE(strength);
  inv_sq_strength = 1/SQ(strength);

  x = Cube_x_center - (CubeSize/2);
  y = Cube_y_center - (CubeSize/2);
  z = Cube_z_center - (CubeSize/2);

  for (i=0;i<SubDiv;i++,x+=delta)
  {
   for (j=0;j<SubDiv;j++,y+=delta)
   {
    for (k=0;k<SubDiv;k++,z+=delta)
    {
     effect = 0;
     for (n=0;n<NumBlobs;n++)
     {
      dist = DIST( x,y,z, Blobs[n].x, Blobs[n].y, Blobs[n].z );
      if (dist <= strength)
      {
       effect += 1.0 + A*CUBE(dist)*inv_cube_strength/*/CUBE(strength)*/
                     + B*SQ(dist)*inv_sq_strength/*/SQ(strength)*/
                     + C*dist/strength;
      }
     }
     // printf("%i,%i,%i = %f\n",i,j,k,effect);
     Cube[i][j][k] = effect;
    }
   }
  }
 }


